package mx.unam.ciencias.edd.proyecto3;

public class Colores{
    
    public String colores(int i){
	if(i == 0)
	    return "red";
	if(i== 1)
	    return "green";
	if(i == 2)
            return "orange";
	if(i== 3)
            return "yellow";
	if(i == 4)
            return "blue";
	if(i== 5)
            return "indigo";
	if(i == 6)
            return "pink";
	if(i== 7)
            return "coral";
	if(i == 8)
            return "turquoise";
        if(i== 9)
            return "lightblue";
	return "";
    }

}